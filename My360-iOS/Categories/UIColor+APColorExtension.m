#import "UIColor+APColorExtension.h"

@implementation UIColor (APColorExtension)



+ (UIColor *)darkBlue {
    
    return [UIColor colorWithRed:0.007 green:0.102 blue:0.168 alpha:1];
}


+ (UIColor *)navBarBlue{

    return [UIColor colorWithRed:0.2500 green:0.3945 blue:0.5859 alpha:1];
}


+ (UIColor *)transparentBlackColor {  //#
    
    return [UIColor colorWithRed:0.0 green:0.0 blue:0.0 alpha:0.2];
}




@end
